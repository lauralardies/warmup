# A la hora de realizar operaciones no se le asocia el valor de string a los números.
# Esto se debe a que cuando lo imprimimos, todo string se escribe literalmente en la consola. 
# Es decir, si creamos el código print("21 + 43"), al ejecutarlo en la consola aparecería lo siguiente: 21 + 43
# Igualmente, si programamos print("21" + "43"), en la consola se mostraría el siguiente resultado: 2143
# Por lo tanto, para que phyton calcule operaciones debemos codificar los valores numéricos sin las comillas, es decir, estos no pueden ser string.